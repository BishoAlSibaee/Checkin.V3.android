package com.example.hotelservicesstandalone;

public class CurtainControl {

    String ControlDP;
    String Open;
    String Close;
    String Stop;
    String Continue;

    public CurtainControl(String controlDP, String open, String close, String stop, String aContinue) {
        ControlDP = controlDP;
        Open = open;
        Close = close;
        Stop = stop;
        Continue = aContinue;
    }
}
